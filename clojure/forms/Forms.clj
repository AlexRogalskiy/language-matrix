; Chunks of data read by the clojure reader.

; A number is a form
(println 22)

; A Vector is a form
(println [1 2 3])

; A List is a form
(println 1 2 3)

; A List with a function is also a form
; Having the + in the beginning is prefix notation
(println (+ 1 2 4))

;A function with no arguments returns 0
(println (+))

